
public class Variables {

	public static void main(String[] args) {

		int x = 4;
		x = 3;

		System.out.println(x);
		System.out.println(x + x);

		x = x + 4;

		System.out.println(x);

		String words;

		words = "5";

		System.out.println(words);
		
		words = "This is a sentence";

		System.out.println(words + " that is not saying much.");

	}

}
